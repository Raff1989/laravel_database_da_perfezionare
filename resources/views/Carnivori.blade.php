            <h1>Specie Carnivore</h1>
            <p>I carnivori sono un ordine di mammiferi euteri. Si dividono in due sottordini: i caniformi (canidi, orsi, mustelidi, foche ecc.) e i feliformi (felidi, iene, manguste ecc.), che si distinguono soprattutto per differenze nella struttura interna delle bolle timpaniche. Di origine paleocenica, i carnivori divennero i mammiferi predatori più diffusi dopo aver spodestato i creodonti durante l'Eocene, in gran parte grazie alla loro adattabilità e dentatura multiuso.
                Quasi tutte le specie dispongono di denti carnassiali,sebbene siano assenti nei pinnipedi. Oltre la dentatura, tutti i carnivori condividono tratti come la visione binoculare, un mantello relativamente denso, e artigli che variano in grandezza e retrattilità. Hanno un olfatto ben sviluppato, associato con ghiandole odorifere usate nella demarcazione del territorio. La visione e l'udito sono anch'essi molto acuti.
                Tuttavia, la variabilità di grandezza nei carnivori, dalla donnola pesante 30 grammi all'elefante marino pesante 4 000 chili, supera quella di tutti gli altri mammiferi, inclusi i roditori. La varietà di stili locomotori e le scelte di habitat sono ugualmente variabili: ci sono nuotatori specializzati come i pinnipedi e le lontre, specie arboricole come il panda rosso e il cercoletto, scavatori come i tassi, e corridori come i lupi, i licaoni e le iene.
                I carnivori sono distribuiti globalmente tra i due poli, sopravvivendo in deserti, foreste tropicali e boreali, savane, tundra, e acqua dolce e salata.</p>
